package com.qst.vo;

import java.util.List;

public class MyCollectVO {

	private int myCollectId;
	private int goodsId;
	private String goodsImagePath;
	private String goodsName;
	private Double goodsPrice;
	
	
	@Override
	public String toString() {
		return "MyCollectVO [myCollectId=" + myCollectId + ", goodsId="
				+ goodsId + ", goodsImagePath=" + goodsImagePath
				+ ", goodsName=" + goodsName + ", goodsPrice=" + goodsPrice
				+ "]";
	}

	public int getMyCollectId() {
		return myCollectId;
	}

	public void setMyCollectId(int myCollectId) {
		this.myCollectId = myCollectId;
	}

	public String getGoodsImagePath() {
		return goodsImagePath;
	}

	public void setGoodsImagePath(String goodsImagePath) {
		this.goodsImagePath = goodsImagePath;
	}

	public String getGoodsName() {
		return goodsName;
	}

	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}

	public Double getGoodsPrice() {
		return goodsPrice;
	}

	public void setGoodsPrice(Double goodsPrice) {
		this.goodsPrice = goodsPrice;
	}

	public int getGoodsId() {
		return goodsId;
	}

	public void setGoodsId(int goodsId) {
		this.goodsId = goodsId;
	}
	
	
	
}
